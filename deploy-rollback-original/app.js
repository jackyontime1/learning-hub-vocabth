function speak(text, lang, voice, pause = 90) {
  return new Promise(resolve => {
    const utterance = new SpeechSynthesisUtterance(text);
    utterance.lang = lang;
    utterance.voice = voice;
    utterance.rate = state.rate;
    utterance.pitch = 0.92;
    utterance.volume = 1;
    utterance.onend = () => setTimeout(resolve, pause);
    utterance.onerror = () => setTimeout(resolve, pause);
    speechSynthesis.speak(utterance);
  });
}

function wait(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

async function pauseForRecall(prompt, lang = "th-TH", voice = null) {
  if (!state.recallMode || !state.isPlaying) return;
  await speak(prompt, lang, voice, 120);
  if (!state.isPlaying) return;
  await wait(state.recallPauseMs);
}

async function pauseForEnglishMeaningRecall(word) {
  if (!state.recallMode || !state.isPlaying) return;
  await speak("คำว่า", "th-TH", null, 80);
  if (!state.isPlaying) return;
  await speak(word, "en-US", state.enVoice, 80);
  if (!state.isPlaying) return;
  await speak("แปลว่าอะไร", "th-TH", null, 120);
  if (!state.isPlaying) return;
  await wait(state.recallPauseMs);
}

async function speakItem(item) {
  if (!item) return;
  if (item.mode === "review") {
    await pauseForRecall(`คำว่า ${item.meaning} ภาษาอังกฤษคืออะไร`);
    if (!state.isPlaying) return;
    await speak(item.word, "en-US", state.enVoice, 120);
    await speak(item.meaning, "th-TH", null, 280);
    return;
  }
  await pauseForEnglishMeaningRecall(item.word);
  if (!state.isPlaying) return;
  await speak(item.word, "en-US", state.enVoice, 140);
  if (state.spellingEnabled) {
    await speakSpelling(item.word);
  }
  if (!state.isPlaying) return;
  await speak(item.word, "en-US", state.enVoice, 180);
  await speak(item.meaning, "th-TH", null, 260);
  if (!item.hasCuratedExample) return;
  await speak(item.example, "en-US", state.enVoice, 120);
  await speak(item.exampleTh, "th-TH", null, 340);
}

function formatTime(seconds) {
  if (!Number.isFinite(seconds)) return "0:00";
  const mins = Math.floor(seconds / 60);
  const secs = Math.floor(seconds % 60);
  return `${mins}:${String(secs).padStart(2, "0")}`;
}

function updateLessonAudioProgress() {
  const audio = getAudioPlayer();
  if (!audio || state.playbackMode !== "mp3") return;
  const duration = audio.duration;
  const current = audio.currentTime;
  if (Number.isFinite(duration) && duration > 0) {
    if (findLessonAudioEntry()?.playbackMode === "item-sequence") {
      const overall = ((state.index + (current / duration)) / Math.max(1, state.queue.length)) * 100;
      els.progressBar.style.width = `${Math.min(100, overall)}%`;
      els.progressText.textContent = `${state.index + 1} / ${state.queue.length} · ${formatTime(current)} / ${formatTime(duration)}`;
    } else {
      els.progressBar.style.width = `${Math.min(100, (current / duration) * 100)}%`;
      els.progressText.textContent = `${formatTime(current)} / ${formatTime(duration)}`;
    }
  } else {
    els.progressText.textContent = "MP3";
  }
}

function handleSystemAudioPause(audio) {
  if (getAudioPlayer() !== audio) return;
  if (state.suppressAudioPauseEvent) {
    state.suppressAudioPauseEvent = false;
    return;
  }
  if (!state.isPlaying || audio.ended) return;
  state.isPlaying = false;
  state.isPaused = true;
  renderNow();
}

function handleSystemAudioPlay(audio) {
  if (getAudioPlayer() !== audio) return;
  state.isPlaying = true;
  state.isPaused = false;
  renderNow();
}

function releaseAudio(audio) {
  if (!audio) return;
  state.suppressAudioPauseEvent = true;
  audio.pause();
  audio.removeAttribute("src");
  audio.load();
  queueMicrotask(() => {
    state.suppressAudioPauseEvent = false;
  });
}

function stopLessonAudio() {
  const audio = getAudioPlayer();
  state.activeAudioEntry = null;
  if (audio) releaseAudio(audio);
}

function loadCurrentItemAudio(entry, audio) {
  const item = state.queue[state.index];
  const file = findItemAudioFile(entry, item);
  if (!file) {
    state.audioManifestError = `MP3 missing for ${item?.word || "word"}`;
    stopLessonAudio();
    state.playbackMode = "speech";
    state.isPaused = false;
    playSpeechFromCurrent();
    return;
  }

  state.isPlaying = true;
  state.isPaused = false;
  state.playbackMode = "mp3";
  state.activeAudioEntry = entry;
  renderNow();
  audio.preload = "auto";
  audio.playbackRate = state.rate;
  setAudioSource(audio, file);
  audio.play().catch(() => {
    if (getAudioPlayer() !== audio) return;
    state.audioManifestError = "MP3 play blocked";
    stopLessonAudio();
    state.playbackMode = "speech";
    state.isPaused = false;
    playSpeechFromCurrent();
  });
}

function playItemSequence(entry) {
  stopLessonAudio();
  speechSynthesis.cancel();
  const audio = getAudioPlayer();
  loadCurrentItemAudio(entry, audio);
}

function playLessonAudio(entry) {
  stopLessonAudio();
  speechSynthesis.cancel();
  state.isPlaying = true;
  state.isPaused = false;
  state.playbackMode = "mp3";
  state.activeAudioEntry = entry;
  renderNow();

  const audio = getAudioPlayer();
  audio.preload = "auto";
  audio.playbackRate = state.rate;
  setAudioSource(audio, entry.file);

  audio.play().catch(() => {
    if (!state.isPlaying || state.playbackMode !== "mp3") return;
    state.audioManifestError = "MP3 play blocked";
    stopLessonAudio();
    state.playbackMode = "speech";
    playSpeechFromCurrent();
  });
}

function handleAudioEnded() {
  const audio = getAudioPlayer();
  const entry = state.activeAudioEntry;
  if (!audio || !entry || state.playbackMode !== "mp3") return;
  if (entry.playbackMode === "item-sequence") {
    state.index += 1;
    if (state.index >= state.queue.length) {
      if (state.repeat) {
        state.index = 0;
      } else {
        state.index = Math.max(0, state.queue.length - 1);
        state.isPlaying = false;
        state.isPaused = false;
        state.playbackMode = "speech";
        stopLessonAudio();
        renderNow();
        return;
      }
    }
    loadCurrentItemAudio(entry, audio);
    return;
  }
  state.isPlaying = false;
  state.isPaused = false;
  state.playbackMode = "speech";
  state.activeAudioEntry = null;
  state.index = Math.min(state.queue.length - 1, Math.max(0, state.queue.length - 1));
  renderNow();
}

function handleAudioError() {
  if (!state.activeAudioEntry || state.playbackMode !== "mp3") return;
  state.audioManifestError = "MP3 load failed";
  stopLessonAudio();
  state.playbackMode = "speech";
  state.isPaused = false;
  playSpeechFromCurrent();
}

function initAudioPlayer() {
  const audio = getAudioPlayer();
  if (!audio) return;
  state.audioPlayer = audio;
  audio.preload = "none";
  audio.addEventListener("loadedmetadata", updateLessonAudioProgress);
  audio.addEventListener("timeupdate", updateLessonAudioProgress);
  audio.addEventListener("pause", () => handleSystemAudioPause(audio));
  audio.addEventListener("play", () => handleSystemAudioPlay(audio));
  audio.addEventListener("ended", handleAudioEnded);
  audio.addEventListener("error", handleAudioError);
}

async function playSpeechFromCurrent() {
  if (!state.queue.length) return;
  stopLessonAudio();
  state.playbackMode = "speech";
  state.isPlaying = true;
  state.isPaused = false;
  renderNow();
  while (state.isPlaying && state.index < state.queue.length) {
    renderNow();
    await speakItem(state.queue[state.index]);
    if (!state.isPlaying) break;
    state.index += 1;
    if (state.index >= state.queue.length && state.repeat) state.index = 0;
  }
  state.isPlaying = false;
  state.isPaused = false;
  speechSynthesis.cancel();
  state.index = Math.min(state.index, Math.max(0, state.queue.length - 1));
  renderNow();
}

async function playFromCurrent() {
  if (!state.queue.length) return;
  const lessonAudio = findLessonAudioEntry();
  if (lessonAudio) {
    if (lessonAudio.playbackMode === "item-sequence") {
      playItemSequence(lessonAudio);
    } else {
      playLessonAudio(lessonAudio);
    }
    return;
  }
  await playSpeechFromCurrent();
}

function stopPlayback() {
  state.isPlaying = false;
  state.isPaused = false;
  stopLessonAudio();
  state.playbackMode = "speech";
  speechSynthesis.cancel();
  renderNow();
}

function pausePlayback() {
  const audio = getAudioPlayer();
  if (state.playbackMode !== "mp3" || !audio) {
    stopPlayback();
    return;
  }
  audio.pause();
}

function resumePlayback() {
  const audio = getAudioPlayer();
  if (!audio || !state.isPaused) {
    playFromCurrent();
    return;
  }
  audio.playbackRate = state.rate;
  audio.play().then(() => {
    state.isPlaying = true;
    state.isPaused = false;
    renderNow();
  }).catch(() => {
    state.audioManifestError = "MP3 play blocked";
    stopPlayback();
  });
}

if ("mediaSession" in navigator) {
  navigator.mediaSession.setActionHandler("play", () => {
    if (state.isPaused) {
      resumePlayback();
      return;
    }
    if (!state.isPlaying) {
      playFromCurrent();
    }
  });
  navigator.mediaSession.setActionHandler("pause", () => {
    if (state.isPlaying) {
      pausePlayback();
    }
  });
  navigator.mediaSession.setActionHandler("stop", () => {
    stopPlayback();
  });
  navigator.mediaSession.setActionHandler("previoustrack", () => {
    const continuePlaying = state.isPlaying || state.isPaused;
    stopPlayback();
    state.index = Math.max(0, state.index - 1);
    renderNow();
    if (continuePlaying) playFromCurrent();
  });
  navigator.mediaSession.setActionHandler("nexttrack", () => {
    const continuePlaying = state.isPlaying || state.isPaused;
    stopPlayback();
    state.index = Math.min(state.queue.length - 1, state.index + 1);
    renderNow();
    if (continuePlaying) playFromCurrent();
  });
}

function exportCsv() {
  const header = "word,thai_meaning,example,example_thai,mode\n";
  const body = state.queue.map(item => {
    return [item.word, item.meaning, item.example, item.exampleTh, item.mode]
      .map(value => `"${String(value).replaceAll('"', '""')}"`)
      .join(",");
  }).join("\n");
  const blob = new Blob([header + body], { type: "text/csv;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = `oxford-audio-day-${state.day}.csv`;
  link.click();
  URL.revokeObjectURL(url);
}

