function makeEntry(word, order) {
  const lower = cleanWord(word.toLowerCase());
  const custom = customEntries().find(item => item.key === lower);
  if (custom) return custom;
  const packed = thaiPack[lower];
  const oxfordMeta = state.oxfordMetadata?.entries?.[lower] || null;
  const translation = state.oxfordTranslations?.entries?.[lower] || null;
  const category = oxfordMeta?.category || (packed ? packed[4] : inferCategory(lower));
  const meaning = translation?.meaning || packed?.[1] || "ยังไม่มีคำแปลไทย";
  const example = packed?.[2] || "";
  const exampleTh = translation?.exampleTh || packed?.[3] || "";
  const normalizedMeaning = translation?.meaning || packed?.[1] || "ยังไม่มีคำแปลไทย";
  const normalizedExampleTh = translation?.exampleTh || packed?.[3] || "";
  const normalizedCuratedExample = Boolean((translation?.hasCuratedExample && example && normalizedExampleTh) || (packed?.[2] && packed?.[3]));
  return {
    word: cleanWord(word),
    pron: packed ? packed[0] : approximateThaiPron(word),
    meaning: packed ? packed[1] : "ยังไม่มีคำแปลไทยใน starter pack",
    example: packed ? packed[2] : `I want to remember the word ${cleanWord(word)}.`,
    exampleTh: packed ? packed[3] : "ฉันอยากจำคำนี้ให้ได้",
    category,
    ready: Boolean(packed),
    order,
    frequencyRank: oxfordMeta?.categoryRank || Number.MAX_SAFE_INTEGER,
    meaning: normalizedMeaning,
    example,
    exampleTh: normalizedExampleTh,
    ready: Boolean(normalizedMeaning),
    hasCuratedExample: normalizedCuratedExample
  };
}

