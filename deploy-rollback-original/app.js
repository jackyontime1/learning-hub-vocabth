function spellingHtml(word, compact = false) {
  const chars = word.replace(/\s+/g, "").split("").slice(0, 18);
  if (!chars.length) return "";
  return `<span class="spelling-strip${compact ? " mini" : ""}" aria-label="spelling">${chars.map(char => `<span class="letter-box">${char}</span>`).join("")}</span>`;
}

async function speakSpelling(word) {
  const chars = word.replace(/\s+/g, "").split("");
  for (const char of chars) {
    const utterance = new SpeechSynthesisUtterance(char);
    utterance.lang = "en-US";
    utterance.voice = state.enVoice;
    utterance.rate = Math.max(0.55, state.rate - 0.18);
    utterance.pitch = 0.86;
    utterance.volume = 1;
    await new Promise(resolve => {
      utterance.onend = () => setTimeout(resolve, 260);
      utterance.onerror = () => setTimeout(resolve, 260);
      speechSynthesis.speak(utterance);
    });
    if (!state.isPlaying) return;
  }
  await new Promise(resolve => setTimeout(resolve, 180));
}

