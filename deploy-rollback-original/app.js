function findLessonAudioEntry() {
  const lessons = state.audioManifest?.lessons || {};
  const direct = lessons[lessonIdFor(state.category, state.day)];
  const fallback = Object.values(lessons).find(entry => {
    return entry.category === state.category && Number(entry.day) === state.day;
  });
  const entry = direct || fallback || null;
  if (!entry || entry.status !== "generated") return null;
  const source = `${entry.provider || ""} ${state.audioManifest?.generatedBy || ""}`;
  if (/mms|windows/i.test(source)) return null;
  if (entry.playbackMode === "item-sequence" && Array.isArray(entry.items)) {
    return entry;
  }
  const selectedFile = state.spellingEnabled
    ? (entry.fileWithSpelling || entry.file)
    : entry.fileWithoutSpelling;
  if (!selectedFile) return null;
  return { ...entry, file: versionAudioFile(selectedFile, entry.generatedAt) };
}

function versionAudioFile(file, generatedAt) {
  if (!file) return "";
  const version = generatedAt ? encodeURIComponent(generatedAt) : "";
  return version ? `${file}${file.includes("?") ? "&" : "?"}v=${version}` : file;
}

function findItemAudioFile(entry, item) {
  if (!entry || entry.playbackMode !== "item-sequence" || !item) return "";
  const audioItem = entry.items.find(row => cleanWord(row.word).toLowerCase() === cleanWord(item.word).toLowerCase());
  if (!audioItem) return "";
  const file = state.spellingEnabled
    ? audioItem.fileWithSpelling
    : audioItem.fileWithoutSpelling;
  return versionAudioFile(file, entry.generatedAt);
}

function findPendingLessonAudioEntry() {
  const lessons = state.audioManifest?.lessons || {};
  const direct = lessons[lessonIdFor(state.category, state.day)];
  return direct && direct.status !== "generated" ? direct : null;
}

