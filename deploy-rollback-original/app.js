function cleanWord(raw) {
  return raw.replace(/\s+[12]$/, "").replace(/[^\w\s.'-]/g, "").trim();
}

function inferCategory(word) {
  const lower = word.toLowerCase();
  const known = thaiPack[lower];
  if (known) return known[4];
  if (/\b(airport|road|station|travel|tour|hotel|city|country|north|south|river|beach|park)\b/.test(lower)) return "Travel & Places";
  if (/\b(food|bread|coffee|tea|water|kitchen|room|house|home|table|bed|bath|cook|eat|drink)\b/.test(lower)) return "Home & Food";
  if (/\b(health|heart|body|animal|plant|air|weather|water|pain|doctor|medicine|energy)\b/.test(lower)) return "Nature & Health";
  if (/\b(work|study|school|teacher|student|business|company|computer|data|learn|book|class)\b/.test(lower)) return "Work & Study";
  if (/\b(friend|family|mother|father|child|happy|sad|angry|love|feel|person|people)\b/.test(lower)) return "People & Feelings";
  if (/\b(media|music|art|voice|video|photo|news|culture|society|phone)\b/.test(lower)) return "Society & Media";
  if (/\b(make|take|give|get|think|choose|start|stop|change|use|find|build|create)\b/.test(lower)) return "Actions & Thinking";
  return "Starter Everyday";
}

